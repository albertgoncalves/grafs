#!/usr/bin/env python

from os import environ

from matplotlib.pyplot import close, savefig, subplots, tight_layout


def plot(points, edges, filename):
    _, ax = subplots(figsize=(8, 8))
    ax.scatter(*zip(*points))
    for edge in edges:
        a = edge[0]
        b = edge[1]
        x = (a[0], b[0])
        y = (a[1], b[1])
        ax.plot(x, y, lw=0.85)
    ax.set_aspect("equal")
    tight_layout()
    savefig(filename)
    close()


